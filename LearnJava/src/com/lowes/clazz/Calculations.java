package com.lowes.clazz;
/**
 * 
 * @author Lowes
 *
 */
public class Calculations {
	/**
	 * Used for Addition
	 * @param firstValue
	 * @param secondValue
	 * @return
	 */
	public int add(int firstValue,int secondValue) {
		return firstValue + secondValue;
	}
	
	/**
	 * Used for Subtraction
	 * @param firstValue
	 * @param secondValue
	 * @return
	 */
	public int sub(int firstValue,int secondValue) {
		return firstValue - secondValue;
	}
	
	/**
	 * Used for Multiply
	 * @param firstValue
	 * @param secondValue
	 * @return
	 */
	protected int mul(int firstValue,int secondValue) {
		return firstValue * secondValue;
	}
	
}
